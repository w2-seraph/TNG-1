<h3>Contact formulier</h3>
<p>Als u ons inlichtingen verstrekt via het contact formulier, zullen de gegevens van het vragen formulier, inclusief de details die u daar heeft verstrekt, opgeslagen worden om het verzoek af te handelen en om u vervolg vragen te kunnen stellen. Natuurlijk geven we die gegevens niet door aan derden.</p>

