<h3>Cookies</h3>
<p>Este sitio web usa cookies. Cookies que no dañan su computador y no contienen virus. Las cookies cumplen una importante funcion en la operación de este sitio. Las Cookies son pequeños textos que no son almacenados en su computador por el navegador.</p>

<p>La mayoria de las cookies que usamos son "session cookies". Son automaticamente borradas cuando usted cierra el navegador. Otras cookies son almacenadas en su computador hasta que usted las borra. Las cookies nos permiten reconocer su navegador en la proxima visita y restaurar algunas de las preferencias (como su idioma preferido).</p>

<p>Usted puede configurar su navegador de manera que usted sea informado de las cookies y permitirlas en ciertos casos. Desabilitar las cookies puede limitar la funcionalidad de este sitio web.</p>

