<h3>Cookies</h3>
<p>Ce site Web utilise des cookies. Les cookies n'endommagent pas votre ordinateur et ne contiennent pas de virus. Les cookies remplissent des fonctions importantes dans le fonctionnement de ce site. Les cookies sont de petits fichiers texte qui sont stockés sur votre ordinateur par votre navigateur.</p>

<p>La plupart des cookies que nous utilisons sont des "cookies de session". Ils sont automatiquement supprimés lorsque vous fermez votre navigateur. D'autres cookies restent stockés sur votre appareil jusqu'à ce que vous les supprimiez. Ces cookies nous permettent de reconnaître votre navigateur lors de votre prochaine visite et de restaurer certaines de vos préférences (comme votre langue préférée).</p>

<p>Vous pouvez paramètrer votre navigateur pour être informé de la configuration des cookies et n'autoriser les cookies que dans certains cas. La désactivation des cookies peut limiter la fonctionnalité de ce site Web..</p>

