<h3>Data beskyttelse</h3>
<p>Ejerne af dette websted tager beskyttelsen af personlige data meget alvorligt. Vi behandler personlige oplysninger fortroligt og
i overensstemmelse med de lovbestemte databeskyttelsesbestemmelser og privatlivslove.</p>

<p>Brugen af vores hjemmeside er mulig uden at give personlige oplysninger. Når der kræves personlige oplysninger (for eksempel navn og
e-mail-adresse på kontaktformularen), gives dette frivilligt. Disse data vil ikke blive videregivet til en tredjepart.</p>

<p>Vi påpeger, at afsendelse af information via internettet er underlagt sikkerhedsproblemer.
Komplet beskyttelse af dataene fra adgang til tredjeparter er ikke mulig.</p>

<h3>Cookies</h3>
<p>Denne hjemmeside bruger cookies. Cookies beskadiger ikke computeren og indeholder ikke virusser. Cookies udfører vigtige funktioner
i driften af dette websted. Cookies er små tekstfiler, der bliver gemt på computeren af browseren.p>

<p>De fleste af de cookies, der bliver brugt på dette websted, er "session cookies". De slettes automatisk, når browseren lukkes. Andre cookies
forbliver gemt, indtil de bliver slettet "manuelt". Disse cookies giver webstedet mulighed for at genkende browseren, næste gang webstedet besøges,
og nogle af brugerens præferencer bliver gendannet (f.eks. sprog indstillingen).</p>

<p>Man kan konfigurere browserindstillingerne, således at man bliver informeret om indstillingen af cookies og kun tillade cookies i visse tilfælde.
Deaktivering af cookies kan begrænse funktionaliteten på dette websted.</p>

<h3>Server logfiler</h3>
<p>Udbyderen af dette websted indsamler og gemmer automatisk information i serverlogfiler, som browseren sender til os. Disse er:</p>

	<ul>
		<li>Browser type og version</li>
		<li>Operativ system</li>
		<li>Henviser URL</li>
		<li>Værtsnavn på adgangscomputeren</li>
		<li>IP-adresse (anonymiseret)</li>
		<li>Tidspunkt for anmodning</li>
	</ul>
<p>Disse data kan ikke tildeles bestemte personer. Disse data vil ikke blive slået sammen med andre datakilder.
Vi forbeholder os ret til at kontrollere disse data med tilbagevirkende kraft, hvis vi bliver opmærksomme på specifikke indikationer af ulovlig brug.</p>

<h3>Kontaktformular</h3>
<p>Hvis man henvender sig til os via kontaktformularen, bliver oplysninger fra forespørgselsformularen, herunder kontaktoplysninger, gemt for at behandle henvendelsen og i tilfælde af opfølgende spørgsmål. Disse data bliver ikke videregivet.</p>

<h3>Ret til information, aflysning, blokering</h3>
<p>Brugeren har ret til at få oplysninger om gemte personoplysninger, deres oprindelse og modtagere, og formålet med enhver behandling eller transmission, sammen med retten til at korrigere, blokere eller slette disse data. For at få flere oplysninger om personlige data, kan man <a href="suggest.php">kontakte os når som helst</a>.</p>
