<h3>Dataskydd</h3>
<p>Ägarna till den här webbplatsen tar skyddet av ditt personliga data seriöst. Vi behandlar ditt personliga data konfidentiellt och
i överensstämmelse med lagstadgade regler om dataskydd och denna integritetspolicy.</p>

<p>Användade av vår webbplats är möjlig utan att uppge privat data. När personlig information efterfrågas (till exempel namn och
	e-postadress på kontaktformuläret), lämnas det frivilligt. Detta data lämnas aldrig över till tredje part.</p>

<p>Vi vill påpeka att information skickad över Internet alltid är föremål för säkerhetsproblem.
	Ett totalt skydd mot åtkomst av datat från tredje part är inte möjligt.</p>

<h3>Kakor/Cookies</h3>
<p>Webbplatsen använder kakor (eng. "cookies"). Kakor kan inte skada din dator och innehåller inte virus. Kakor utför många viktiga
	uppgifter för hur webbplatsen fungerar och upplevs. Kakor är små textfiler som lagras på din dator av din webbläsare.</p>

<p>De flesta kakorna är "sessionskakor". De raderas automatiskt när du stänger webbläsaren. Andra kakor förblir lagrade på din dator
	(telefon, surfplatta) tills du raderar dem. De kakorna gör det möjligt för oss att känna igen din webbläsare nästa gång du
	besöker oss och återskapar en del inställningar (som t.ex. ditt valda språk).</p>

<p>Du kan ställa in webbläsaren så att du informeras om kakornas inställningar och bara tillåta kakor i utvalda fall.
	Att helt inaktivera kakor kan begränsa funktionen av webbplatsen.</p>

<h3>Logg-filer på servern</h3>
<p>Leverantören av webbplatsens sidor samlar automatiskt information i logg-filer på servern, som din webbläsare
	förmedlar till oss. Det är:</p>

	<ul>
		<li>Webbläsarens namn och version</li>
		<li>Operativsystem</li>
		<li>Vilken URL du kom från</li>
		<li>Värdnamnet för den inkopplade datorn</li>
		<li>IP-adressen (anonymiserad)</li>
		<li>Datum och klockslag för kontakten</li>
	</ul>
<p>Dessa data kan inte härledas till en specifik person. Dessa data kommer inte att kombineras med annat data från andra källor.
	Vi reserverar rättigheten att kontrollera datat i efterhand, om vi får indikationer på illegalt användande.</p>

<h3>Kontaktformulär</h3>
<p>Om du skickar oss förfrågningar via kontaktformuläret, kommer dina uppgifter på formuläret, inklusive kontaktinformationen du lämnar,
	att lagras för att kunna behandla frågan och i händelse av följdfrågor. Vi vidarebefordrar naturligtvis inget sådant data till någon.</p>

<h3>Rätt till information, annullering och blockering</h3>
<p>Du har rätt att när som helst erhålla information om ditt lagrade personliga data, ursprung och mottagare och vad
	syftet är för bearbetning eller överföring, samtidigt som du kan begära korrigering, blockering eller radering av detta data.
	För vidare information om personligt data kan du använda <a href="suggestindex.php">Kontaktformuläret när som helst</a>.</p>
