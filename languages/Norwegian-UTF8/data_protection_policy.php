<h3>Personvern</h3>
<p>Eieren av dette nettstedet tar personvernet ditt på ytterste alvor. Vi behandler dine persondata konfidensielt og i samsvar med lovbestemte databeskyttelsesforskrifter og retningslinjene her på siden.</p>

<p>Bruken av vårt nettsted er mulig uten å gi personopplysninger. Når det blir bedt om personlig informasjon (for eksempel navn og epost-adresse på kontaktskjema; 'Ta kontakt'), er dette frivillig for deg. Disse dataene vil ikke sendes videre til tredjepart.</p>

<p>Vi påpeker at sending av informasjon over Internett er underlagt sikkerhetsproblemer. 
	Fullstendig beskyttelse av dataene fra å få tilgang til tredjeparter er ikke mulig.</p>

<h3>Informasjonskapsler ('Cookies')</h3>
<p>Dette nettstedet bruker informasjonskapsler. Informasjonskapsler skader ikke datamaskinen din og inneholder ikke virus. Informasjonskapsler utfører viktige funksjoner
	i driften av dette nettstedet. Informasjonskapsler er små tekstfiler som lagres på din datamaskin 
	av din nettleser.</p>

<p>De fleste av informasjonskapslene vi bruker er sesjonskapsler ('session cookies'). De slettes automatisk når du lukker nettleseren din. Andre informasjonskapsler 
	forblir lagret på enheten din til du sletter dem. Disse informasjonskapslene gir oss mulighet til å gjenkjenne nettleseren din neste gang du besøker nettstedet
	og gjenoppretter noen av dine preferanser (for eksempel ditt foretrukne språk).</p>

<p>Du kan konfigurere nettleserinnstillingene dine slik at du blir informert om innstillingen av informasjonskapsler og bare tillate informasjonskapsler i bestemte tilfeller. 
	Deaktivering av informasjonskapsler kan begrense funksjonaliteten til dette nettstedet.</p>

<h3>Loggfiler på vertsmaskin</h3>
<p>Vertsmaskinleverandøren for dette nettstedet kan automatisk samle og lagre informasjon i loggfiler, som nettleseren din 
	overfører. Dette kan være:</p>

	<ul>
		<li>Nettlesertype og versjon</li>
		<li>Operativsystem</li>
		<li>Refererende URL</li>
		<li>Vertsnavnet for din datamaskin</li>
		<li>IP adresse (anonymisert)</li>
		<li>Tidspunktet for ditt besøk</li>
	</ul>
<p>Disse dataene kan ikke kobles til bestemte personer. Disse dataene vil ikke bli slått sammen med andre datakilder. 
	Vi forbeholder oss retten til å kontrollere og ettergå disse dataene om nødvendig, hvis vi blir gjort oppmerksomme på indikasjoner om ulovlig bruk.</p>

