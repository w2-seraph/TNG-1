<h3>Cookies</h3>
<p>Tyto internetové stránky používají cookies.  Cookies jsou malé textové soubory, které do vašeho počítače ukládá váš prohlížeč internetu.
Cookies plní důležité funkce při provozu těchto internetových stránek. Cookies nepoškozují váš počítač a neobsahují viry.</p>

<p>Většina cookies, které používáme, jsou tzv. session cookies. Po zavření vašeho prohlížeče internetu se automaticky odstraní. Jiné cookies
zůstanou ve vašech zařízení uloženy, dokud je neodstraníte. Tyto cookies umožňují rozpoznat váš prohlížeč při příštím návštěvě
a obnovit některé z vašich předvoleb (například preferovaný jazyk).</p>  

<p>Svůj prohlížeč internetu můžete nakonfigurovat tak, abyste byli informováni o nastavení cookies a povolili cookies pouze v určitých případech.
Zakázání cookies může omezit funkčnost těchto internetových stránek.</p>  

