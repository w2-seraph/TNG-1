                <a name="what"><p class="subheadbold">Was sind Verbindungen?</p></a>

                <p>Eine <strong>Verbindung</strong> ist eine Aufzeichnung einer Beziehung zwischen zwei Personen in Ihrer Datenbank,
                die m&ouml;glicherweise nicht zur regul&auml;ren Stammbaumstruktur Ihrer Genealogie geh&ouml;ren. Tats&auml;chlich sind es zwei Personen, die z.B. in einem Verein verbunden aber nicht miteinander verwandt sind.</p>

        </td>
</tr>
<tr class="databack">
        <td class="tngshadow">

                <a name="add"><p class="subheadbold">Verbindungen hinzuf&uuml;gen/bearbeiten/l&ouml;schen</p></a>

                <p>Um eine Verbindung zu einer Person hinzuzuf&uuml;gen, zu bearbeiten oder zu l&ouml;schen, suchen Sie die Person im Bereich Verwaltung/Personen und bearbeiten
                den Personen-Datensatz, dann klicken Sie auf die Schaltfl&auml;che "Verbindungen" am oberen Bildschirmrand (falls bereits Verbindungen vorhanden sind,
                wird ein gr&uuml;ner Punkt neben der Schaltfl&auml;che zu sehen sein). Wenn Sie auf die Schaltfl&auml;che klicken, wird ein Fenster ge&ouml;ffnet, in dem
                alle Verbindungen f&uuml;r die Person angezeigt werden.</p>

                <p>Um eine neue Verbindung hinzuzuf&uuml;gen, klicken Sie auf die Schaltfl&auml;che "Hinzuf&uuml;gen" und f&uuml;llen das Formular aus. </p>

                <p>Um eine bestehende Verbindung zu bearbeiten oder zu l&ouml;schen, klicken Sie auf die entsprechende Schaltfl&auml;che neben der Verbindung.</p>

                <p>While adding or editing an association, take note of the following

                Beim Hinzuf&uuml;gen oder Bearbeiten einer Verbindung, beachten Sie Folgendes:</p>

                <span class="optionhead">Personen ID</span>
                <p>Geben Sie die ID der Person, die mit der aktiven Person verbunden werden soll, oder klicken Sie auf die Schaltfl&auml;che Suchen, um die ID zu suchen.</p>

                <span class="optionhead">Verwandtschaft</span>
                <p>Geben Sie die Art der Verbindung zwischen den beiden Personen an. Zum Beispiel, <em>Taufpate</em>, <em>Mentor</em> oder <em>Zeuge</em>.</p>

                <p>Wenn Sie mit dem Hinzuf&uuml;gen, Bearbeiten oder L&ouml;schen von Verbindungen dieser Person fertig sind, klicken Sie auf die Schaltfl&auml;che "Fertig", um das Fenster zu schlie&szlig;en.</p>

