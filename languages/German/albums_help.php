                <a name="sort"><p class="subheadbold">Alben sortieren</p></a>
                <p>Standardm&auml;&szlig;ig werden Alben mit einer Person, Familie, Quelle, Archiv oder Orte in der Reihenfolge, in der sie mit diesem Objekt verkn&uuml;pft waren, sortiert. Um diese
                 Sortierordnung zu &auml;ndern, m&uuml;ssen Sie eine neue Sortierordnung auf der Seite Album / Sortieren angeben.</p>

                <span class="optionhead">Stammbaum, Verkn&uuml;pfungs-Type, Sammlung:</span>
                <p>W&auml;hlen Sie den Stammbaum mit den verkn&uuml;ften Eintr&auml;gen, f&uuml;r die Sie die Alben sortieren m&ouml;chten. Als n&auml;chstes w&auml;hlen Sie einen Verkn&uuml;pfungs-Typ (Person, Familie, Quelle, Archiv oder Ort) und
                 die Sammlung, die Sie sortieren m&ouml;chten.</p>

                <span class="optionhead">ID:</span>
                <p>Geben Sie die ID-Nummer oder den Namen (nur Orte) des Eintrags ein. Wenn Sie die ID-Nummer oder die genaue Ortsbezeichnung nicht kennen, klicken Sie auf das Lupen-Symbol, um danach zu suchen.
                 Wenn Sie das gew&uuml;nschte Objekt gefunden haben, klicken Sie auf "Ausw&auml;hlen", neben diesem Eintrag. Das Popup wird geschlossen und die ausgew&auml;hlte ID wird im ID-Feld erscheinen.</p>

                <span class="optionhead">Sortierverfahren</span>
                <p>Nach der Auswahl oder Eingabe einer ID klicken Sie auf "Weiter", um alle Alben f&uuml;r das ausgew&auml;hlte Objekt und Sammlung in der aktuellen Reihenfolge anzuzeigen.
                 Um die Sortierfolge der Alben zu &auml;ndern, klicken Sie im Bereich "Sortieren" auf jedes Objekt und halten die Maustaste gedr&uuml;ckt, w&auml;hrend Sie den Mauszeiger an die gew&uuml;nschte Position
                 innerhalb der Liste ziehen. Wenn das Element den gew&auml;hlten Punkt erreicht hat, lassen Sie die Maustaste los ("drag and drop"). &Auml;nderungen werden automatisch an diesem Punkt gespeichert.</p>

