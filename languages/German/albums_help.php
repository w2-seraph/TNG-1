                <a name="add"><p class="subheadbold">Alben hinzuf&uuml;gen</p></a>
                <p>Ein <strong>Album</strong> in TNG ist eine Gruppierung von Medien. Ein Album kann eine beliebige Anzahl von Medien enthalten. Ein einzelnes Medium kann zu mehreren Alben geh&ouml;ren.
                 Wie einzelne Medien, k&ouml;nnen Alben mit Personen, Familien, Quellen, Archiven oder Orten verkn&uuml;pft werden.</p>

                <p>Um ein neues Album hinzuzuf&uuml;gen, klicken Sie auf <strong>Hinzuf&uuml;gen</strong>. Dann f&uuml;llen Sie das Formular aus. Einige Informationen, einschließlich der Medien, die mit Personen, Familien und anderen Objekten verkn&uuml;pft werden sollen, k&ouml;nnen nach dem Speichern des Eintrags hinzugefügt werden. Beachten Sie Folgendes:</p>

                <p><span class="optionhead">Album-Name</span><br />
                Der Name Ihres Albums.</p>

                <p><span class="optionhead">Beschreibung</span><br />
                Eine kurze Beschreibung des Albums oder der darin enthaltenen Objekte.</p>

                <p><span class="optionhead">Schl&uuml;sselw&ouml;rter</span><br />
                Eine beliebige Anzahl von Schl&uuml;sselw&ouml;rtern außerhalb des Albumnamens oder der Beschreibung, um dieses Album bei einer Suche zu finden.</p>

                <p><span class="optionhead">Erforderliche Felder:</span> Albumname ist ein Pflichtfeld. Es ist aber in Ihrem eigenen Interesse, die anderen Felder ebenfalls auszuf&uuml;llen.</p>

