                <p style="float:right"><a href="#top">Nach oben</a></p>
                <a name="edit"><p class="subheadbold">Bestehende Alben bearbeiten</p></a>
                <p>Um &Auml;nderungen an einem bestehenden Album vorzunehmen, verwenden Sie <a href="#search">Suche</a>, um das Album zu finden. Dann klicken Sie auf das Symbol Bearbeiten neben dem Album.
                 Beachten Sie die folgenden Punkte, die nicht auf der Seite "Hinzuf&uuml;gen" enthalten sind:</p>

                <span class="optionhead">Album-Medien</span>
                <p>Um Medien zu einem Album hinzuzuf&uuml;gen, klicken Sie auf "Hinzuf&uuml;gen". Dann benutzen Sie das Formular, um Medien aus der Datenbank auszuw&auml;hlen. Um dies zu erreichen, wählen Sie eine Auflistung und / oder einen Stammbaum (beides optional) und geben dann einen Teil des Medien-Namens oder der Beschreibung in das Feld "Suche nach" ein
                 und klicken dann auf die Schaltfl&auml;che "Suche". Wenn Sie ein Element gefunden haben, das Sie gerne dem Album hinzuf&uuml;gen m&ouml;chten, klicken Sie auf "Hinzufügen" links neben dem Medium. Das
                 Medium wird hinzugefügt, das Popup bleibt erhalten. Wiederholen Sie diesen Schritt, um weitere Medien hinzuzuf&uuml;gen oder klicken Sie auf "Fenster schlie&szlig;en", um zur Seite "Album bearbeiten" zur&uuml;ckzukehren.</p>

                <p>Um ein Medien aus einem Album zu entfernen, bewegen Sie den Mauszeiger &uuml;ber das Objekt. Es erscheint ein Link "Entfernen". Klicken Sie auf diesen Link, um das Medium zu entfernen. Nach der
                 Best&auml;tigung, wird das Medium verblassen.</p>

                <p>Um ein Vorschaubild als <strong>Standard-Foto</strong> f&uuml;r das aktuelle Album zu w&auml;hlen, bewegen Sie den Mauszeiger &uuml;ber das Medium. Es erscheint ein Link "Als Standard setzen".
                 Klicken Sie auf diesen Link, um die Miniaturansicht f&uuml;r dieses Element als das Standard-Foto des Albums zu bestimmen. Um ein anderes Foto als Standard-Foto zu bestimmen, wiederholen Sie den Vorgang mit einem anderen
                 Eintrag in der Liste. Um das Standard-Foto zu l&ouml;schen, klicken Sie auf "Standard-Foto l&ouml;schen" am oberen Rand der Seite.</p>

                <p>Um die Medien innerhalb des Albums zu ordnen, klicken Sie in der Spalte "Sortieren" auf das Objekt und halten die Maustaste gedr&uuml;ckt. Dann ziehen Sie den Mauszeiger an die gew&uuml;nschte Position
                 der Liste. Wenn das Element den gew&uuml;nschten Punkt erreicht hat, lassen Sie die Maustaste los ("drag and drop"). &Auml;nderungen werden automatisch an diesem Punkt gespeichert.</p>

                <p>Sie k&ouml;nnen ein beliebiges Medium auch direkt an die Spitze der Liste setzen, indem Sie auf den doppelten Pfeil, rechts neben dem "Drag"-Bereich, klicken.</p>

                <span class="optionhead">Album-Verkn&uuml;pfungen</span>
                <p>Sie k&ouml;nnen dieses Album mit Personen, Familien, Quellen, Archiven oder Orten verkn&uuml;pfen. F&uuml;r jeden Link w&auml;hlen Sie zun&auml;chst den Stammbaum, der mit dem Objekt verkn&uuml;pft werden soll.
                 Als n&auml;chstes w&auml;hlen Sie den Verkn&uuml;pfungs-Typ (Person, Familie, Quelle, Archiv oder Ort) und schlie&szlig;lich geben Sie die ID-Nummer oder den Namen (nur Orte) des zu verkn&uuml;pfenden Objekts ein. Nachdem
                 alle Informationen eingegeben sind, klicken Sie auf die Schaltfl&auml;che "Hinzuf&uuml;gen".</p>

                <p>Wenn Sie die ID-Nummer oder genaue Orts-Namen nicht kennen, klicken Sie auf das Lupen-Symbol, um danach zu suchen. Ein Popup-Fenster erscheint, damit Sie die Suche beginnen k&ouml;nnen.
                 Wenn Sie die gew&uuml;nschte Objekt-Beschreibung gefunden haben, klicken Sie auf "Hinzuf&uuml;gen", auf der linken Seite. Sie k&ouml;nnen auf "Hinzuf&uuml;gen" f&uuml;r mehrere Eintr&auml;ge klicken. Wenn Sie mit der Erstellung von Verkn&uuml;pfungen fertig sind, klicken Sie auf "Fenster schlie&szlig;en".</p>

                <p><b>HINWEIS</b>: Alle &Auml;nderungen in Bezug auf Album-Medien und  Album-Verkn&uuml;pfungen werden sofort gespeichert und erfordern nicht, dass Sie auf die Schaltfl&auml;che "Speichern" am unteren Rand des Bildschirms klicken.
                 &Auml;nderungen im Bereich "Album Information" m&uuml;ssen mit Klick auf "Speichern" abgeschlossen werden.</p>

