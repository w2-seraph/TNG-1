                <p>Um Notiten f&uuml;r eine Person, Familie, Quelle, ein Archiv oder Ereignis hinzuzuf&uuml;gen, zu bearbeiten oder zu l&ouml;schen, klicken Sie auf das Symbol "<b>Notizen</b>" am oberen Rand des Bildschirms oder neben jedem Ereignis (wenn Notizen bereits existieren,
