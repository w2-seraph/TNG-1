                <a name="add"><p class="subheadbold">Neue Eintr&auml;ge hinzuf&uuml;gen</p></a>
                <p>Mit der Funktion <strong>Gesuchte Angaben</strong> k&ouml;nnen Sie eine Liste der kritischen Personen oder Fotos, die Ihnen Probleme bei der Erforschung bereiten, erstellen.
                 Die Liste ist in zwei Kategorien unterteilt, <strong>Personen mit fehlenden Angaben</strong> und <strong>Fotos mit unbekannten Personen</strong>. Um einen neuen Eintrag zu einer dieser
                 Kategorien zu erstellen, klicken Sie auf "Hinzuf&uuml;gen" unter der entsprechenden Position. Dann f&uuml;llen Sie das Formular aus. Beachten Sie Folgendes:</p>

                <span class="optionhead">Titel</span>
                <p>Geben Sie Ihrem Eintrag einen Titel, der eigentlich eine Frage sein kann. Zum Beispiel: <em><b>Wer ist diese Person?</b></em> oder <em><b>Wer ist John Carlisle's Vater?</b></em></p>

                <span class="optionhead">Beschreibung</span>
                <p>F&uuml;gen Sie Ihrem Eintrag auch eine kurze Beschreibung bei. Diese k&ouml;nnte aus aktuellen Erkenntnissen bestehen, die Sie gesammelt haben.</p>

                <span class="optionhead">Stammbaum</span>
                <p>Falls gew&uuml;nscht, k&ouml;nnen Sie diesen Eintrag mit einem Stammbaum verkn&uuml;pfen (optional).</p>

                <span class="optionhead">Person</span>
                <p>Wenn dieser Eintrag eng mit einer Person verkn&uuml;pft ist, geben Sie die Personen-ID ein oder klicken Sie auf das Lupen-Symbol, um sie zu suchen. Wenn Sie die gew&uuml;nschte
                 Person gefunden haben, klicken Sie auf "<b>Ausw&auml;hlen</b>", um auf die Seite <b>Gesuchte Angaben</b> mit der ausgew&auml;hlten ID zur&uuml;ckzukehren.</p>

                <span class="optionhead">Foto ausw&auml;hlen</span>
                <p>Wenn dieser Eintrag ist eng mit einem Foto verkn&uuml;pft ist, klicken Sie auf "Foto ausw&auml;hlen", um nach dem Foto in den Foto-Datensätzen, die bereits in Ihrer Datenbank enthalten sind, zu suchen. Wenn Sie das gew&uuml;nschte Foto gefunden haben, klicken Sie auf "Ausw&auml;hlen", um zur Seite "<b>Gesuchte Angaben</b>" mit der ausgew&auml;hlten ID zur&uuml;ckzukehren.</p>

                <pWenn Sie fertig sind, klicken Sie auf die Schaltfläche "Speichern", um zur Liste zurückzukehren. Ihr neuer Eintrag wird auf der Unterseite der Kategorie hinzugef&uuml;gt.</p>

