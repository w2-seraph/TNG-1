		<a name="gettingstarted"><p class="subheadbold">Erste Schritte:</p></a>
		<p>Nicht sicher, was Du zuerst tun solltest? Hier sind die Grundlagen:</p>
		<ol>
				<li><p><strong>Folge sorgf&auml;ltig den Installationsanweisungen in der <a href="../../readme.html" target="_blank">readme.html</a> Datei.</strong>
          Alle wesentlichen Konfigurationsschritte k&ouml;nnen direkt von dieser Seite erledigt werden, aber Du kannst auch alle n&ouml;tigen Einstellungen hier im Administrations-Men&uuml; (<strong>Verwaltung</strong>) vornehmen.</p></li>
				<li><p><strong>Erstelle mindestens einen Stammbaum.</strong> Solange Du nur eine unabh&auml;ngige GEDCOM-Datei hast, brauchst Du auch nur einen Stammbaum.</p></li>
				<li><p><strong>Erstelle mindestens einen Benutzer.</strong> Der erste Benutzer den Du angelegst, muss ein Administrator sein (mu&szlig; alle Rechte haben und darf NICHT einem Stammbaum zugeordnet sein).</p></li>
				<li><p><strong>Importiere Deine Daten oder gib diese manuell ein</strong>. Falls Du die Daten eintippst, ist es gleichg&uuml;ltig, was Du zuerst eingibst.
					Einige m&ouml;gen es lieber, zuerst die Personen einzugeben und diese dann mit Familien zu verkn&uuml;pfen, andere beginnen lieber mit den Familien und bauen den Stammbaum auf diese Weise auf.</p></li>
				<li><p><strong>Sieh dir die nachfolgenden Prozesse an</strong> (unter Import/Export) und f&uuml;hre die aus, von denen Du denkst, da&szlig; diese notwendig sind (mehr Informationen: in der dortigen Hilfe).</p></li>
				<li><p><strong>Medien.</strong> Wenn Deine Daten eingegeben oder importiert sind, kannst Du beginnen, Fotos, Dokumente und andere Medien einzuf&uuml;gen. Viel Spa&szlig; dabei!</p></li>
				<li><p><strong>Alles andere.</strong> Du wirst Dich sicherlich auch f&uuml;r die anderen Bereiche des Verwaltungs-Men&uuml;s interessieren. Zus&auml;tzliche Hilfe findest Du auf jeder Seite.</p></li>
		</ol>

