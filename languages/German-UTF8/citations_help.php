                <a name="what"><p class="subheadbold">Was sind Zitate?</p></a>

                <p>Ein <strong>Zitat</strong> ist ein Verweis auf eine Quelle, mit der Absicht des Nachweises der Richtigkeit einer bestimmten Information. Die Quelle
                beschreibt im Allgemeinen, wo die Information gefunden wurde (zB ein Buch oder eine Volksz&auml;hlung), w&auml;hrend das Zitat in der Regel genauere Informationen enth&auml;lt (zB auf welcher Seite).
                Die gleiche Quelle kann mehrfach f&uuml;r verschiedene Personen, Familien, Notizen und Ereignisse genannt werden.</p>


