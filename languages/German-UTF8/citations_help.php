                existiert nur in einem anderen Stammbaum. Wechseln Sie zu Verwaltung/Quellen und erstellen Sie die Quelle im richtigen Stammbaum, dann wechseln Sie wieder zu den Zitaten und w&auml;hlen die Quelle.</p>

