                <p>Um Zitate hinzuzuf&uuml;gen, zu bearbeiten oder zu l&ouml;schen, klicken Sie auf das Zitaten-Symbol oben auf dem Bildschirm oder neben einer Notiz oder einem Ereignis (wenn es bereits Zitate gibt,
