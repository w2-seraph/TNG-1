                <p>Um eine neue Notiz hinzuzuf&uuml;gen, klicken Sie auf "Hinzuf&uuml;gen" und f&uuml;llen das Formular aus. </p>

                <p>Um eine vorhandene Notiz zu bearbeiten oder zu l&ouml;schen, klicken Sie auf das entsprechende Symbol links neben dieser Notiz.</p>

